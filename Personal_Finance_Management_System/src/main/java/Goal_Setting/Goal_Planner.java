    private void Save_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Save_ButtonActionPerformed
        boolean valid = true;
        //new border for invalid inputs
        Border border = BorderFactory.createLineBorder(Color.RED, 2, true);
        
        if(Check_Input.checkEmpty(jTextField1.getText())){
            valid = false;
            jTextField1.setBorder(border);
            jTextField1.addFocusListener(new FocusAdapter() {
            @Override
            public void focusGained(FocusEvent e) {
                jTextField1.setBorder(new JTextField().getBorder());
            }});
        }
                    
        if(Check_Input.checkEmpty((String) jComboBox1.getSelectedItem())){
            valid = false;
            jComboBox1.setBorder(border);
            jComboBox1.setEditable(false);
            jComboBox1.addFocusListener(new FocusAdapter() {
            @Override
            public void focusGained(FocusEvent e) {
                    jComboBox1.setBorder(new JComboBox<String>().getBorder());
                    jComboBox1.setEditable(true);
            }});
        }
        
        if (Check_Input.checkNumeric(jTextField2.getText())){
            valid = false;
            jTextField2.setBorder(border);
            jTextField2.addFocusListener(new FocusAdapter() {
            @Override
            public void focusGained(FocusEvent e) {
                jTextField2.setBorder(new JTextField().getBorder());
            }});
        }
        if(!valid)
            JOptionPane.showMessageDialog(null, "Invalid Input", "Warning", JOptionPane.ERROR_MESSAGE);
        else{
            Date javaDate = jDateChooser1.getDate();
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String Date = dateFormat.format(javaDate);
            //insert data to the database and jTable
            try {
                GoalDBCreator.Insert_Data(
                        GoalDBCreator.id,
                        jTextField1.getText(),
                        String.valueOf(jComboBox1.getSelectedItem()),
                        jTextField2.getText(),
                        Date,
                        jTextArea1.getText());
            } catch (SQLException e) {
                System.out.println("Data Insertion has failed");
                JOptionPane.showMessageDialog(null, "Database insertion error", "Warning", JOptionPane.ERROR_MESSAGE);
                return;
            }
            //add new row to Table model
            GoalDBCreator.model.addRow( new Object[]{
                                        jTextField1.getText(),
                                        String.valueOf(jComboBox1.getSelectedItem()),
                                        jTextField2.getText(),
                                        Date,
                                        jTextArea1.getText()});
            //notify the jTable with the changes made in the Table model
            GoalDBCreator.model.fireTableDataChanged();
            //rest the feilds after Save process
            Reset_Components.Reset_feilds(jTextField1, jTextField2, jTextArea1, jComboBox1, jDateChooser1);
        }
            
    }//GEN-LAST:event_Save_ButtonActionPerformed

